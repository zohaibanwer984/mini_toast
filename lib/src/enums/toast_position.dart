/// Represents the vertical position of a toast on the screen.
enum ToastVerticalPosition {
  /// Positions the toast at the top of the screen.
  top,

  /// Positions the toast at the bottom of the screen.
  bottom,
}

/// Represents the horizontal position of a toast on the screen.
enum ToastHorizontalPosition {
  /// Positions the toast on the left side of the screen.
  left,

  /// Positions the toast at the center of the screen.
  center,

  /// Positions the toast on the right side of the screen.
  right,
}
